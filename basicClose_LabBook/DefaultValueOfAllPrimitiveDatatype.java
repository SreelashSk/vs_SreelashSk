package com.basicClose_LabBook;

public class DefaultValueOfAllPrimitiveDatatype {
	static boolean b;
	static char ch;
	static byte bt;
	static short s;
	static int a;
	static long l;
	static float f;
	static double d;
	
	public static void main(String[] args) {
		System.out.println("Default Value of boolean is: "+b);
		System.out.println("Default Value of char is: "+ch);
		System.out.println("Default Value of byte is: "+bt);
		System.out.println("Default Value of short is: "+s);
		System.out.println("Default Value of int is: "+a);
		System.out.println("Default Value of long is: "+l);
		System.out.println("Default Value of float is: "+f);
		System.out.println("Default Value of double is: "+d);
	}
}
